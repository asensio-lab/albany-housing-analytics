def fix_addr(series):
    for key, val in addr_fix_typos.items():
        series = series.replace(key, val)
    for key, val in addr_fix_entries.items():
        if series == key:
            series = val
    return series

def fix_series(series):
    series = series.apply(fix_addr)
    return series
